import { cva, type VariantProps } from 'class-variance-authority'
import type * as React from 'react'

import { cn } from '@/lib/utils'

const badgeVariants = cva(
  'inline-flex items-center rounded-md border px-2.5 py-0.5 text-xs font-semibold transition-colors focus:outline-none focus:ring-2 focus:ring-(--jp-vermilion) focus:ring-offset-2',
  {
    variants: {
      variant: {
        default:
          'border-transparent bg-(--jp-vermilion) text-white shadow hover:bg-(--jp-vermilion)/80',
        secondary: 'border-transparent bg-(--jp-paper) text-(--jp-ink) hover:bg-(--jp-paper)/80',
        outline: 'border-(--jp-mist) bg-transparent text-(--jp-stone)',
      },
    },
    defaultVariants: {
      variant: 'default',
    },
  }
)

export interface BadgeProps
  extends React.HTMLAttributes<HTMLDivElement>,
    VariantProps<typeof badgeVariants> {}

function Badge({ className, variant, ...props }: BadgeProps) {
  return <div className={cn(badgeVariants({ variant }), className)} {...props} />
}

export { Badge, badgeVariants }
